#import "UIColor+TemplateColor.h"

@implementation UIColor (TemplateColor)

+ (UIColor *)navigationBarColor
{
    return [UIColor colorWithRed:215.0f/255.0f green:212.0f/255.0f blue:214.0f/255.0f alpha:1];
}

+ (UIColor *)fontColor
{
    return [UIColor colorWithRed:135.0f/255.0f green:143.0f/255.0f blue:163.0f/255.0f alpha:1];
}

@end
